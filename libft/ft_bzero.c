#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*cast_s;

	cast_s = (unsigned char *) s;
	i = 0;
	while (i < n)
		cast_s[i++] = 0;
}
